    private void changePass(String OldPass,String NewPassword){
            String result = quanlycuahangsach.quanlycuahangsach.AccountBUS.changePassword(this.MaNhanVien,OldPass,NewPassword);
            if(result.contains("Mật khẩu không khớp")){
                GUI.SweetAlert.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi","Mật khẩu không khớp", 0); 
            }
            else{
            GUI.SweetAlert.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Thành công","Đổi mật khẩu thành công", 0); 
            ((javax.swing.JFrame) SwingUtilities.getWindowAncestor(quanlycuahangsach.quanlycuahangsach.bg)).setEnabled(true);
            this.dispose();
            }
     
    }
