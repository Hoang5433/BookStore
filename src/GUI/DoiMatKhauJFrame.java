public void validation(){
         if(txtNhapLaiMatKhau.getText().isEmpty() || txtMatKhauMoi.getText().isEmpty()){
            GUI.SweetAlert.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi","Không được bỏ trống", 0);
        } 
        else if(!txtNhapLaiMatKhau.getText().equals(txtMatKhauMoi.getText())){
            GUI.SweetAlert.SweetAlert.showSweetAlert(new javax.swing.JFrame(),"Lỗi","Mật khẩu nhập lại không khớp", 0);
        } 
        else 
            changePass(txtMatKhauCu.getText(),txtMatKhauMoi.getText());   
}
