    public void add(){
    if(GUI.SweetAlert.SweetAlert.showSweetOption(this, "Thêm sản phẩm","Xác nhạn thêm?", 0)==1){
        if(txtTenChuongTrinh.getText().equals("")){
        GUI.SweetAlert.SweetAlert.showSweetAlert(this, "Thông báo", "Không được bỏ trống", 1);
            return;
        }
        ChuongTrinhKhuyenMaiDTO ChuongTrinhKhuyenMai  = new ChuongTrinhKhuyenMaiDTO(lblMaChuongTrinhKhuyenMai.getText(),txtTenChuongTrinh.getText(),DateTime.getCurrentTimestamp());
               if(quanlycuahangsach.quanlycuahangsach.ChuongTrinhKhuyenMaiBUS.add(ChuongTrinhKhuyenMai)){
        ((javax.swing.JFrame) SwingUtilities.getWindowAncestor(quanlycuahangsach.quanlycuahangsach.bg)).setEnabled(true);
       GUI.KhuyenMai.ChuongTrinhKhuyenMaiJPanel.fetchAll();
       this.dispose();
        lblMaChuongTrinhKhuyenMai.setText(quanlycuahangsach.quanlycuahangsach.ChuongTrinhKhuyenMaiBUS.getLatest());
               }
           }
    }
